export function getSavedLanguage() {
  if (typeof window === 'undefined') {
    return 'en';
  }

  const savedLanguage = window.localStorage.getItem(LANGUAGE_STORAGE_KEY);
  return SUPPORTED_LANGUAGES[savedLanguage] ? savedLanguage : 'en';
}

export function saveLanguage(language) {
  if (typeof window === 'undefined') {
    return;
  }

  const safeLanguage = SUPPORTED_LANGUAGES[language] ? language : 'en';
  window.localStorage.setItem(LANGUAGE_STORAGE_KEY, safeLanguage);
  window.dispatchEvent(new CustomEvent('sakura-language-change', { detail: safeLanguage }));
}

export function translateKey(language, key) {
  return translations[language]?.[key] || translations.en[key] || key;
}

export function translateText(language, value) {
  if (value === null || value === undefined) {
    return value;
  }

  return textTranslations[language]?.[value] || value;
}
